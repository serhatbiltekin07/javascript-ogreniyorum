/* 
        !=  --> eşit değil mi
        >   : büyük mü
        <   : küçük mü
        >=  : büyük eşit mi
        <=  : küçük eşit mi
*/

console.log(5 != 3); // 5 üç'e eşit değildir --> true
let yas = 23;
console.log(yas != 23); //false
console.log(11 < 12); //true
console.log(12 > 10); //true
console.log(12 <= 12); //true
